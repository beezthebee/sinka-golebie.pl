					Nazywam się Adam Sinka i mieszkam wraz z rodziną w Katowicach, gdzie w przydomowym kompleksie gołębników ogrodowych prowadzę swoją hodowlę gołębi pocztowych. Moja przygoda z gołębiami rozpoczęła się w okresie dzieciństwa, kiedy jako mały chłopiec pomagałem swojemu ojcu (Leopoldowi) w nowej hodowli (była to jego druga hodowla, gdyż pierwszą mimo licznych sukcesów w latach 70-tych musiał zlikwidować z powodów finansowych, aby móc utrzymać rodzinę). To właśnie ojciec - doświadczony hodowca, nauczył mnie jak prowadzić hodowlę i udzielił mi wielu cennych porad i wskazówek, które wykorzystuję do dnia dzisiejszego.  Gołębie od razu mnie zainteresowały i bardzo chętnie towarzyszyłem ojcu podczas codziennych czynności hodowlanych, koszowaniu gołębi oraz podczas lotów. Kiedy w 1997 roku wstąpiłem do PZHGP i mogłem lotować wraz z ojcem, moja pasja przynosiła mi coraz większą radość. Kilkukrotnie zdobyliśmy tytuł Mistrza Sezonu w Oddziale Katowice  124. W roku 2003 zawarliśmy wspólnotę hodowlaną z kolegą hodowcą Andrzejem Jochaniakiem, z którym zdobyliśmy kilkukrotnie Mistrzostwo w Lotach Gołębi Młodych w Oddziale Katowice 124 oraz wielokrotnie zdobywaliśmy mistrzostwo w poszczególnych kategoriach.
					
